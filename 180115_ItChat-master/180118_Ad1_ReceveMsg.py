import itchat

####################################################################################

@itchat.msg_register(itchat.content.TEXT)
def print_content(msg):
    if msg['Text'] == '1': # It: the received must be char or chars. not number
        print('You have choose option 1:')
    elif msg['Text'] == '2':
        print('You have choose option 2:')
    else:
        print(msg['Text'])

