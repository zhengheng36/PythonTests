import itchat

####################################################################################

@itchat.msg_register(itchat.content.TEXT)
def print_content(msg):
    if msg['Text'] == '1': 
        strSend = u'你选择了第一个'
        itchat.send(strSend, 'filehelper')  
        print(strSend)
    elif msg['Text'] == '2':
        strSend2 = u'你选择了第二个'
        itchat.send(strSend2, 'filehelper')
        print(strSend2)
    else:
        print(msg['Text'])

